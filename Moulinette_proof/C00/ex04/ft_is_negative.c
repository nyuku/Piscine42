#include <unistd.h>

void	ft_is_negative(int n)
{
	char	pos;
	char	neg;

	pos = 'P';
	neg = 'N';
	if (n >= 0)
	{
		write(1, &pos, sizeof(pos));
	}
	else
	{
		write(1, &neg, sizeof(neg));
	}
}
